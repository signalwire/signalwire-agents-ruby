# frozen_string_literal: true

require 'minitest/autorun'
require 'rack/test'
require 'json'

ENV['SIGNALWIRE_LOG_MODE'] = 'off'

require_relative '../lib/signalwire_agents'

class AuthAutoGeneratedTest < Minitest::Test
  def test_basic_auth_auto_generated
    saved_user = ENV.delete('SWML_BASIC_AUTH_USER')
    saved_pass = ENV.delete('SWML_BASIC_AUTH_PASSWORD')
    begin
      agent = SignalWireAgents::AgentBase.new
      creds = agent.get_basic_auth_credentials
      assert_equal 2, creds.length
      refute_empty creds[0]
      refute_empty creds[1]
    ensure
      ENV['SWML_BASIC_AUTH_USER'] = saved_user if saved_user
      ENV['SWML_BASIC_AUTH_PASSWORD'] = saved_pass if saved_pass
    end
  end

  def test_auto_generated_creds_are_uuid_format
    saved_user = ENV.delete('SWML_BASIC_AUTH_USER')
    saved_pass = ENV.delete('SWML_BASIC_AUTH_PASSWORD')
    begin
      agent = SignalWireAgents::AgentBase.new
      user, pass = agent.get_basic_auth_credentials
      assert_match(/\A[0-9a-f-]{36}\z/, user)
      assert_match(/\A[0-9a-f-]{36}\z/, pass)
    ensure
      ENV['SWML_BASIC_AUTH_USER'] = saved_user if saved_user
      ENV['SWML_BASIC_AUTH_PASSWORD'] = saved_pass if saved_pass
    end
  end
end

class AuthExplicitTest < Minitest::Test
  def test_basic_auth_explicit
    agent = SignalWireAgents::AgentBase.new(basic_auth: ['user', 'pass'])
    assert_equal ['user', 'pass'], agent.get_basic_auth_credentials
  end
end

class AuthEnvVarsTest < Minitest::Test
  def test_basic_auth_from_env
    ENV['SWML_BASIC_AUTH_USER']     = 'envuser'
    ENV['SWML_BASIC_AUTH_PASSWORD'] = 'envpass'
    agent = SignalWireAgents::AgentBase.new
    assert_equal ['envuser', 'envpass'], agent.get_basic_auth_credentials
  ensure
    ENV.delete('SWML_BASIC_AUTH_USER')
    ENV.delete('SWML_BASIC_AUTH_PASSWORD')
  end
end

class AuthRackValidationTest < Minitest::Test
  include Rack::Test::Methods

  def app
    @agent = SignalWireAgents::AgentBase.new(basic_auth: ['testuser', 'testpass'])
    @agent.set_prompt_text('Hello')
    @agent.rack_app
  end

  def test_requires_auth
    get '/'
    assert_equal 401, last_response.status
  end

  def test_wrong_auth
    header 'Authorization', 'Basic ' + ['wrong:creds'].pack('m0')
    get '/'
    assert_equal 401, last_response.status
  end

  def test_correct_auth
    header 'Authorization', 'Basic ' + ['testuser:testpass'].pack('m0')
    get '/'
    assert_equal 200, last_response.status
  end

  def test_health_no_auth_needed
    get '/health'
    assert_equal 200, last_response.status
  end

  def test_ready_no_auth_needed
    get '/ready'
    assert_equal 200, last_response.status
  end
end

class AuthTimingSafeTest < Minitest::Test
  def test_secure_compare_is_used
    assert Rack::Utils.respond_to?(:secure_compare),
           'Rack::Utils.secure_compare must be available for timing-safe auth'
  end

  def test_secure_compare_works
    assert Rack::Utils.secure_compare('hello', 'hello')
    refute Rack::Utils.secure_compare('hello', 'world')
    refute Rack::Utils.secure_compare('short', 'longer_string')
  end
end
